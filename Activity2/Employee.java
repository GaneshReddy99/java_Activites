import java.util.*;
class Employee
{
private String name;
private String address;
private String mobile;
public String getname()
{
return name;
}
public String getaddress()
{
return address;
}
public String getmobile()
{
return mobile;
}
void setname(String nam)
{
this.name=nam;
}
void setaddress(String add)
{
this.address=add;
}
void setmobile(String mob)
{
this.mobile=mob;
}
}